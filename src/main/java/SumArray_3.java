public class SumArray_3 {
    /******************************************************************************************************************
     3. Написать алгоритм SumArray, который возвращает сумму всех чисел массива
     Test Data:
     {0, 1, 2, 3, 4, 5} → 15
     {-7, -3} → -10
     */
    public int getSumArray(int[] n) {
        int sum = 0;
        for (int arg : n) {
            sum += arg;
        }
        return sum;
    }
}
